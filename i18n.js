// Current language
let currentLang = localStorage.getItem('language') || 'en';

// Get translation for a key
function getTranslation(key) {
    const keys = key.split('.');
    let value = translations[currentLang];
    
    for (const k of keys) {
        if (value && value[k]) {
            value = value[k];
        } else {
            return key; // Return key if translation not found
        }
    }
    
    return value;
}

// Update all elements with translations
function updatePageLanguage() {
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(key);
        
        // Update text content
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLang;
    
    // Update body data-lang attribute
    document.body.setAttribute('data-lang', currentLang);
    
    // Update page title
    if (currentLang === 'pt') {
        document.title = 'Daniel Hauschild de Aragão | Mentor de Carreira em Análise de Negócios';
    } else {
        document.title = 'Daniel Hauschild de Aragão | Business Analyst Career Mentor';
    }
    
    // Store preference
    localStorage.setItem('language', currentLang);
}

// Switch language
function switchLanguage(lang) {
    if (lang !== currentLang && (lang === 'en' || lang === 'pt')) {
        currentLang = lang;
        updatePageLanguage();
        
        // Update active button
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            }
        });
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    // Set initial language
    updatePageLanguage();
    
    // Set active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === currentLang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
        
        // Add click event
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            switchLanguage(lang);
        });
    });
});

// Export for use in other scripts
window.i18n = {
    getTranslation,
    switchLanguage,
    getCurrentLang: () => currentLang
};


